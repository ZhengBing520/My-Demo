public class User {

    private String name;

    private Integer age;

    private Double driveAge;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public Double getDriveAge() {
        return driveAge;
    }

    public void setDriveAge(Double driveAge) {
        this.driveAge = driveAge;
    }
}
